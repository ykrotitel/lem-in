#include "libft.h"

void	ft_lst_push_front(t_list **alst, void const *cont, size_t cont_size)
{
	t_list *lst;

	if (!(lst = ft_lstnew(cont, cont_size)))
		return ;
	lst->next = *alst;
	*alst = lst;
}
