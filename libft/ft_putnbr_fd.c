#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	str[11];
	int		i;

	i = 0;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		if (n < -9)
		{
			str[i++] = (n % 10) * -1 + 48;
			n = n / 10;
		}
		n *= -1;
	}
	while (n > 9)
	{
		str[i++] = n % 10 + 48;
		n = n / 10;
	}
	str[i] = n % 10 + 48;
	while (i >= 0)
		ft_putchar_fd(str[i--], fd);
}
